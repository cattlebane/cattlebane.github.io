function UIControlBar2 ( player, arg ){
	
	/* ------------------------------------------------------------------------------------------------------------------------------- */
	// MARKUP
	var U = new UIComponent ( arg );
	U.classList.add( 'rvp-controlBar' );
		
	/* ------------------------------------------------------------------------------------------------------------------------------- */
	// PUBLIC PROPERTIES
	U.constant = !!arg.constant;
	U.showOnPoster = !!arg.showOnPoster;

	/* ------------------------------------------------------------------------------------------------------------------------------- */
	// PUBLIC METHODS
	U.toString = function(){
		return '[object UIControlBar2]'
	}

	/* ------------------------------------------------------------------------------------------------------------------------------- */
	// INIT 

	return U;
}